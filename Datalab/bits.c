/*
 * isTmax - returns 1 if x is the maximum, two's complement number,
 *     and 0 otherwise 
 *   Legal ops: ! ~ & ^ | +
 *   Max ops: 10
 *   Rating: 1
 */
int isTmax(int x)
{
  /*
  利用异或性质0异或任何数等于其本身
  如果是Tmax tmp1为0 tmp为全0 取非后为1  return 0^1 = 1 
  如果是-1 tmp为1 tmp为0 取非后为1 return 1^1 = 0
  如果是其他任何数如 2  tmp1为0  tmp2不为0 取非后为0  return 0^0 = 0
  */
  int tmp1 = !(x + 1);
  int tmp2 = !((x + 1) ^ (~x));
  return tmp1 ^ tmp2;
}
