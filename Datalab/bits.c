/* 
 * isAsciiDigit - return 1 if 0x30 <= x <= 0x39 (ASCII codes for characters '0' to '9')
 *   Example: isAsciiDigit(0x35) = 1.
 *            isAsciiDigit(0x3a) = 0.
 *            isAsciiDigit(0x05) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 3
 */
int isAsciiDigit(int x)
{
  /*
  upper 0x39  0011 1001b 加上比0x39大的数后符号由正变负
  lower 0x30  0011 0000b 加上比0x30小的值时是负数
  返回取符号位 全0返回0 否则返回1
  没弄明白 回头再看
  */
  int sign = 0x1 << 31;
  int upper = ~(sign | 0x39);
  int lower = ~0x30;
  upper = sign & (upper + x) >> 31;
  lower = sign & (lower + 1 + x) >> 31;
  return !(upper | lower);
}
/* 
 * conditional - same as x ? y : z 
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z)
{
  /*
  x=true return y else return z
  先对x取非 构造成全0或者全1
  利用逻辑左移加算术右移，可以构造出一个0x0 或者是0xffffffff
  利用获得的x作为掩码 
  进行(z&x),(y&~x)操作 必定有一侧为全0
  利用或操作 (z|y) 选择出需要的数字
  */
  x = (!x << 31) >> 31;
  return (z & x) | (y & ~x);
}
/* 
 * isLessOrEqual - if x <= y  then return 1, else return 0 
 *   Example: isLessOrEqual(4,5) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 24
 *   Rating: 3
 */
int isLessOrEqual(int x, int y)
{
  /*
  先判断符号位
  符号位相同两数相减 再判断符号位
  sign1若为1 则x > y  否则为等于或小于 
  sign2若为1 则x = y  否则为大于或小于 大于在sign1已经判断
  sign3若为1 则x < y  否则为大于或等于 y-x取符号位再取非 不能使用 - 符号
                 
  */
  int signx = x >> 31;
  int signy = y >> 31;
  int sign1 = (signx & !signy);
  int sign2 = !(signx ^ signy);
  int sign3 = !((y + ~x + 1) >> 31);
  return sign1 | (sign2 & sign3);
}

